package com.example.mac.Model;

public class Zones {


    String zone;
    int total, socialD;

    public Zones() {
    }

    public String getZone() {
        return zone;
    }

    public void setZone(String zone) {
        this.zone = zone;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }

    public int getSocialD() {
        return socialD;
    }

    public void setSocialD(int socialD) {
        this.socialD = socialD;
    }


}
